import os
from flask import Flask, session, request, redirect
from flask_session import Session
import spotipy
import uuid
import random
import requests
from bs4 import BeautifulSoup


app = Flask(__name__)
app.config['SECRET_KEY'] = os.urandom(64)
app.config['SESSION_TYPE'] = 'filesystem'
app.config['SESSION_FILE_DIR'] = './.flask_session/'
Session(app)

caches_folder = './.spotify_caches/'
if not os.path.exists(caches_folder):
    os.makedirs(caches_folder)

def session_cache_path():
    return caches_folder + session.get('uuid')

@app.route('/')
def index():
    if not session.get('uuid'):
        # Step 1. Visitor is unknown, give random ID
        session['uuid'] = str(uuid.uuid4())

    cache_handler = spotipy.cache_handler.CacheFileHandler(cache_path=session_cache_path())
    auth_manager = spotipy.oauth2.SpotifyOAuth(scope='user-read-currently-playing playlist-modify-private playlist-modify-public',
                                                cache_handler=cache_handler, 
                                                show_dialog=True)

    if request.args.get("code"):
        # Step 3. Being redirected from Spotify auth page
        auth_manager.get_access_token(request.args.get("code"))
        return redirect('/')

    if not auth_manager.validate_token(cache_handler.get_cached_token()):
        # Step 2. Display sign in link when no token
        auth_url = auth_manager.get_authorize_url()
        return f'<h2><a href="{auth_url}">Sign in</a></h2>'

    # Step 4. Signed in, display data
    spotify = spotipy.Spotify(auth_manager=auth_manager)
    return f'<h2>Hi {spotify.me()["display_name"]}, ' \
           f'<small><a href="/sign_out">[sign out]<a/></small></h2>' \
           f'<a href="/playlists">my playlists</a> | ' \
           f'<a href="/currently_playing">currently playing</a> | ' \
		   f'<a href="/current_user">me</a> | ' \
		   f'<a href="/search">search</a>' \


@app.route('/sign_out')
def sign_out():
    try:
        # Remove the CACHE file (.cache-test) so that a new user can authorize.
        os.remove(session_cache_path())
        session.clear()
    except OSError as e:
        print ("Error: %s - %s." % (e.filename, e.strerror))
    return redirect('/')


@app.route('/playlists')
def playlists():
    cache_handler = spotipy.cache_handler.CacheFileHandler(cache_path=session_cache_path())
    auth_manager = spotipy.oauth2.SpotifyOAuth(cache_handler=cache_handler)
    if not auth_manager.validate_token(cache_handler.get_cached_token()):
        return redirect('/')

    spotify = spotipy.Spotify(auth_manager=auth_manager)
    return spotify.current_user_playlists()


@app.route('/currently_playing')
def currently_playing():
    cache_handler = spotipy.cache_handler.CacheFileHandler(cache_path=session_cache_path())
    auth_manager = spotipy.oauth2.SpotifyOAuth(cache_handler=cache_handler)
    if not auth_manager.validate_token(cache_handler.get_cached_token()):
        return redirect('/')
    spotify = spotipy.Spotify(auth_manager=auth_manager)
    track = spotify.current_user_playing_track()
    if not track is None:
        return track
    return "No track currently playing."


@app.route('/current_user')
def current_user():
    cache_handler = spotipy.cache_handler.CacheFileHandler(cache_path=session_cache_path())
    auth_manager = spotipy.oauth2.SpotifyOAuth(cache_handler=cache_handler)
    if not auth_manager.validate_token(cache_handler.get_cached_token()):
        return redirect('/')
    spotify = spotipy.Spotify(auth_manager=auth_manager)
    return spotify.current_user()

@app.route('/search')
def search():
    cache_handler = spotipy.cache_handler.CacheFileHandler(cache_path=session_cache_path())
    auth_manager = spotipy.oauth2.SpotifyOAuth(cache_handler=cache_handler)
    if not auth_manager.validate_token(cache_handler.get_cached_token()):
        return redirect('/')

    spotify = spotipy.Spotify(auth_manager=auth_manager)

    dict = scr_boomkat()

    track_list = []
    for ar_al in dict:
        track_list.append(get_track(spotify, ar_al['Artist'], ar_al['Album']))
    
    insert_playlist(spotify, track_list)
    
    return spotify.current_user()



def scr_boomkat():
    target_url = 'https://boomkat.com/new-releases'
    ua = 'Mozilla/5.0 (Macintosh; Intel Mac OS X 10_13_5) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/67.0.3396.99 Safari/537.36'
    headers = {'User-Agent': ua}
    r = requests.get(target_url, headers=headers) 
    soup = BeautifulSoup(r.text) 
    art = []
    for ar in soup.find_all('strong'):
        art.append(ar.text)

    alb = []
    for al in soup.find_all("span", attrs={"class": "album-title"}):
        alb.append(al.text)

     
    new_rel_list = []
    for i in range(len(alb)):
        new_rel_list.append(
            {
                'Artist': art[i],
                'Album': alb[i],
            })
            
    return new_rel_list

def insert_playlist(spotify, track_list):

    ids = []
    for t in track_list:
        if len(t) > 0:
            ids.append(t[0]["id"])
    user_id = spotify.current_user()['id']
    spotify.user_playlist_replace_tracks(user=user_id, playlist_id='308vJydaRRDG1suC065Vun', tracks=ids)
    
    return 0


def get_track_random(spotify, al_id):
    results = spotify.album_tracks(al_id)
    tracks = results['items']
    return tracks[random.randint(0, len(tracks)-1)]
    

def get_track(spotify, name, al_name):

    results = spotify.search(q=name, type='album')
    items = results['albums']['items']
    track_list = []
    for it in items:
        if it['name'] ==  al_name:
            print('[INFO]: search spotify library: ok')
            print('[INFO]', it['name'], it['id'])
            track = get_track_random(spotify, it['id'])
            track_list.append(track)
            print('[INFO]', track['name'])
    return track_list

